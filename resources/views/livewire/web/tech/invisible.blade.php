      <span class="eyebrow">How it works</span>
      <h2>Hidden deep in your ear</h2>
      <p>Invisible-in-Canal (IIC) and Completely-in-Canal (CIC) hearing aids are moulded from an impression of your own ear, so each one fits only you. They sit deep in the ear canal, where they stay out of sight.</p>
      <p>Because they rest so close to the eardrum, they use the natural shape of your ear to gather sound — which gives a remarkably natural listening experience, especially on the phone.</p>
